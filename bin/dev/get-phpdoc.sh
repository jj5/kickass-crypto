main() {

  set -euo pipefail;

  pushd "$( dirname "$0" )" >/dev/null;
  pushd "../" >/dev/null;
  source inc/env.sh;
  popd >/dev/null;

  mkdir -p phpdoc;

  pushd "phpdoc/" >/dev/null;

  wget https://phpdoc.org/phpDocumentor.phar

  chmod +x phpDocumentor.phar;

}

main "$@";
